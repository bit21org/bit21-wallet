// Language metadata
export const LANGUAGES = [
  { code: "en", name: "English", nativeName: "English", dir: "ltr" },
  { code: "es", name: "Spanish", nativeName: "Español", dir: "ltr" },
  { code: "zh", name: "Chinese", nativeName: "中文", dir: "ltr" },
  { code: "ar", name: "Arabic", nativeName: "العربية", dir: "rtl" },
  { code: "hi", name: "Hindi", nativeName: "हिन्दी", dir: "ltr" },
  { code: "pt", name: "Portuguese", nativeName: "Português", dir: "ltr" },
];

// Get saved language or detect from browser
export function getLanguage() {
  try {
    const saved = localStorage.getItem("btc_lang");
    if (saved && translations[saved]) return saved;
  } catch {}
  // Auto-detect from browser
  const browserLang = navigator.language?.slice(0, 2);
  if (browserLang && translations[browserLang]) return browserLang;
  return "en";
}

// Save language preference
export function setLanguage(code) {
  localStorage.setItem("btc_lang", code);
}

// Translation function — returns English fallback if key missing
export function t(key, lang) {
  const l = lang || getLanguage();
  return translations[l]?.[key] || translations.en?.[key] || key;
}

// Get direction for current language
export function getDirection(lang) {
  const l = lang || getLanguage();
  const meta = LANGUAGES.find((x) => x.code === l);
  return meta?.dir || "ltr";
}
